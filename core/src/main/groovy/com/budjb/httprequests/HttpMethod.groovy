package com.budjb.httprequests

/**
 * An enumeration of supported HTTP methods.
 */
enum HttpMethod {
    /**
     * HTTP GET.
     */
    GET,

    /**
     * HTTP POST.
     */
    POST,

    /**
     * HTTP PUT.
     */
    PUT,

    /**
     * HTTP DELETE.
     */
    DELETE,

    /**
     * HTTP HEAD.
     */
    HEAD,

    /**
     * HTTP TRACE.
     */
    TRACE,

    /**
     * HTTP OPTIONS.
     */
    OPTIONS
}